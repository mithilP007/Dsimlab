import { toast } from "sonner"

/**
 * Utility function to trigger a native browser file download of a Blob.
 */
function downloadBlob(blob: Blob, filename: string) {
  const url = URL.createObjectURL(blob)
  const link = document.createElement("a")
  link.href = url
  link.setAttribute("download", filename)
  document.body.appendChild(link)
  link.click()
  document.body.removeChild(link)
  URL.revokeObjectURL(url)
}

/**
 * Mock utility exports for the Digital Marketing Simulation reports.
 * All functions return a Promise, simulate latency, and trigger a native download or toast.
 */

export async function exportToCSV(data: any[], filename: string): Promise<boolean> {
  return new Promise((resolve) => {
    toast.info(`Preparing CSV export: ${filename}...`)
    
    setTimeout(() => {
      try {
        // Simple CSV generation
        const headers = Object.keys(data[0] || {})
        const csvRows = [
          headers.join(","),
          ...data.map((row) =>
            headers
              .map((fieldName) => {
                const value = row[fieldName]
                const stringVal = typeof value === "object" ? JSON.stringify(value) : String(value)
                return `"${stringVal.replace(/"/g, '""')}"`
              })
              .join(",")
          ),
        ]
        const csvContent = csvRows.join("\n")
        const blob = new Blob([csvContent], { type: "text/csv;charset=utf-8;" })
        
        downloadBlob(blob, filename.endsWith(".csv") ? filename : `${filename}.csv`)
        toast.success(`CSV Export complete: ${filename}`)
        resolve(true)
      } catch (error) {
        toast.error("Failed to generate CSV export")
        resolve(false)
      }
    }, 1200)
  })
}

